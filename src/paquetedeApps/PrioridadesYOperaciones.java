package paquetedeApps;

public class PrioridadesYOperaciones {

	public static void main(String[] args) {
		int n1 = 5, n2 = 10, suma = n1 + n2*n1, resta = (n1 - n2)*3, prod= n1 * n2+n1;
		double n3 = 5.0 , n4 = 10.0;
		double div = n3 / n4*3.5;
		System.out.println("La suma es: " + suma);
		System.out.println("La resta es: " + resta);
		System.out.println("El producto es: " + prod);
		System.out.println("El cociente es: " + div);

	}

}
